var h_ = 300;

function flat(stHeight, length, otherArg) {
  slope(stHeight, 0, length, otherArg);
}

function slope(stHeight, m, length, otherArg) {
  if (length < 1) {
    length = 1; // Avoid x / 0 errors
  }

  for (var x = 0; x < length; x++) {
    var y = m * x + stHeight;
    append(terrain, y);
    append(terrainR, atan(m));

    h_ = y;

    if (x == 0 && otherArg == 'END') {
      append(checkpoints, [terrain.length, y]);
    }
  }
}

